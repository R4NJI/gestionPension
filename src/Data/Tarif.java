package Data;

/**
 * Class of the table tarif
 */
public class Tarif {

    private String num_tarif;
    private String diplome;
    private String catégorie;
    private int montant;


    public String getNum_tarif() {
        return num_tarif;
    }
    public String getDiplome() {
        return diplome;
    }
    public String getCatégorie() {
        return catégorie;
    }
    public int getMontant() {
        return montant;
    }

}